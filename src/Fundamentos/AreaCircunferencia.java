package Fundamentos;
// AREA DA CIRCUNFERENCIA .
public class AreaCircunferencia {
    public static void main(String[] args) {

        double raio = 3.4;
        final double pi = 3.14159;

        double valorArea = pi * raio * raio;

        System.out.println(valorArea);
        // outra forma.
        System.out.println(pi * raio * raio);

    }
}
